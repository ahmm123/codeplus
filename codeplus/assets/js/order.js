function order() {
  const newBTN = document.querySelector('.new-order-btn');
  const checkBTN = document.querySelector('.check-order-btn');
  const checkOrder = document.querySelector('.check-order');
  const newOrder = document.querySelector('.new-order');
  newBTN.classList.toggle('using');
  checkBTN.classList.toggle('using');
  newOrder.classList.toString('using');
  checkOrder.classList.toString('using');
}